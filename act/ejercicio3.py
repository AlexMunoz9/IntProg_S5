
dia = int(input("Ingrese el dia en forma numerica que sea del 1 al 7:"))

if dia == 1:
    print("Lunes")
elif dia == 2:
    print("Martes")
elif dia == 3:
    print("Miercoles")
elif dia == 4:
    print("Jueves")
elif dia == 5:
    print("Viernes")
elif dia == 6:
    print("Sabado")
elif dia == 7:
    print("Domingo")
else:
    print("El numero digitado no es valido")
    
print("Presiona cero para salir")
    
    